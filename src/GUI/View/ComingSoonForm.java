    public void loadTable(){
        try{
            List<ComingSoon> list = csr.findAll();
            cstm.addList(list);
            table.setModel(cstm);
            cstm.fireTableDataChanged();
        }catch(CrudFormException ex){
            Logger.getLogger(CinemaForm.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
    
    private void tabelaSelectedIndexChange(){
        final ListSelectionModel rowSM = table.getSelectionModel();
        rowSM.addListSelectionListener((ListSelectionEvent e) -> {
            if(e.getValueIsAdjusting()){
                return;
            }
            ListSelectionModel rowSM1 = (ListSelectionModel) e.getSource();
            int selectedIndex = rowSM1.getAnchorSelectionIndex();
            if(selectedIndex > -1){
                ComingSoon cs = cstm.getComingSoon(selectedIndex);
                idField.setText(cs.getId().toString());
                movienameField.setText(cs.getCSMovieName());
                dateField.setDate(cs.getDate());
                ccbm.setSelectedItem(cs.getCinemaID());
                cinemaComboBox.repaint();
            }
        });
    }
    
    public void loadComboBox(){
        try{
            List<Cinema> lista = cr.findAll();
            ccbm.add(lista);
            cinemaComboBox.setModel(ccbm);
            cinemaComboBox.repaint();
        }catch(CrudFormException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }


