    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        try{
            int row = table.getSelectedRow();
            if(row == -1){
                ComingSoon cs = new ComingSoon();
                cs.setCSMovieName(movienameField.getText());
                cs.setDate(dateField.getDate());
                cs.setCinemaID((Cinema)ccbm.getSelectedItem());
                csr.create(cs);
            }else{
                ComingSoon cs = cstm.getComingSoon(row);
                cs.setCSMovieName(movienameField.getText());
                cs.setDate(dateField.getDate());
                cs.setCinemaID((Cinema)ccbm.getSelectedItem());
                csr.edit(cs);
            }
            clear();
            loadTable();
        }catch(CrudFormException ex){
            JOptionPane.showMessageDialog(this, "This movie is already registered!");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

